package com.sp;

public class Pojo {
    private int ID;
    private String NAME;
    private long PHONE_NO;

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getNAME() {
        return NAME;
    }

    public void setNAME(String NAME) {
        this.NAME = NAME;
    }

    public long getPHONE_NO() {
        return PHONE_NO;
    }

    public void setPHONE_NO(long PHONE_NO) {
        this.PHONE_NO = PHONE_NO;
    }
}
